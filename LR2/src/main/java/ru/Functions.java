package ru;

public class Functions {
    public static float one(float x){
        float y;
        double exp = 2.7182818284;
        return y = (float) Math.pow(exp,0.3*x);
    }
    public static float two(float x){
        float y;
        return y = (float) Math.pow(x,2);
    }
    public static float three(float x){
        float y;
        return y = (float) Math.sin(x);
    }
}
